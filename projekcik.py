"""## **Czyszczenie danych i ich imputacja**"""

col_zero = ['flat_rent', 'flat_deposit']
for df in [train, test]:
  df.replace([-999, -9], np.nan, inplace=True)
  df.replace("", np.nan, inplace=True)
  df[col_zero] = df[col_zero].replace(0, np.nan)

bool_cols = ['individual', 'flat_furnished', 'flat_for_students', 'flat_balcony',
             'flat_garage', 'flat_garden', 'flat_dishwasher', 'flat_internet', 'flat_closed_area']
