official_quarters = [
    'Antoninek', 'Zieliniec', 'Kobylepole', 'Chartowo', 'Fabianowo', 'Kotowo',
    'Główna', 'Głuszyna', 'Górczyn', 'Grunwald', 'Jeżyce', 'Junikowo', 'Kiekrz',
    'Krzesiny', 'Pokrzywno', 'Garaszewo', 'Krzyżowniki', 'Smochowice', 'Kwiatowe',
    'Ławica', 'Morasko', 'Radojewo', 'Naramowice', 'Ogrody', 'Piątkowo', 'Podolany',
    'Rataje', 'Sołacz', 'Stare Miasto', 'Winogrady', 'Starołęka', 'Minikowo',
    'Marlewo', 'Strzeszyn', 'Szczepankowo', 'Splawie', 'Krzesinki', 'Świerczewo',
    'Łazarz', 'Umultowo', 'Wilda', 'Winiary', 'Wola', 'Żegrze', 'Dębiec',
    'Nowe Miasto', 'Ostrów Tumski', 'Śródka', 'Zawady', 'Komandoria', 'Łacina'
]
official_quarters = sorted(list(set(official_quarters)), key=len, reverse=True)

area_pattern = r'(\d+(?:[.,]\d+)?)\s*(?:m2|m²|mkw)'

def uzupelnij_area_i_quarter(df):
  def procesuj_wiersz(row):
    title = str(row['ad_title']).lower()

    if pd.isna(row['flat_area']):
      match_obj = re.search(area_pattern, title)
      if match_obj:
        val = float(match_obj.group(1).replace(',', '.'))
        if 10 <= val <= 200:
          row['flat_area'] = val

    if pd.isna(row['quarter']):
      for q in official_quarters:
        if re.search(rf'\b{q.lower()}\b', title):
          row['quarter'] = q
          break
    return row

  return df.apply(procesuj_wiersz, axis=1)

train = uzupelnij_area_i_quarter(train)
test = uzupelnij_area_i_quarter(test)

counts = train['quarter'].value_counts()
popularne = counts[counts >= 10].index

for df in [train, test]:
    df['quarter'] = df['quarter'].apply(lambda x: x if x in popularne else 'Inne')

train['quarter_copy'] = np.nan
test['quarter_copy'] = np.nan

le = LabelEncoder()

mask_train = train['quarter'].notnull()
le.fit(train.loc[mask_train, 'quarter'])

quarter_map = dict(zip(le.classes_, range(len(le.classes_))))

train.loc[mask_train, 'quarter_copy'] = train.loc[mask_train, 'quarter'].map(quarter_map)

mask_test = test['quarter'].notnull()
test.loc[mask_test & ~test['quarter'].isin(quarter_map.keys()), 'quarter'] = np.nan

mask_test = test['quarter'].notnull()
test.loc[mask_test, 'quarter_copy'] = test.loc[mask_test, 'quarter'].map(quarter_map)

it_imp = IterativeImputer(random_state=42, min_value=0)
cols_numeric = ['flat_area', 'flat_rooms', 'quarter_copy', 'building_floor_num']

train[cols_numeric] = it_imp.fit_transform(train[cols_numeric])
test[cols_numeric] = it_imp.transform(test[cols_numeric])

for df in [train, test]:
    df['flat_rooms'] = df['flat_rooms'].round().astype(int)
    df['quarter_copy'] = df['quarter_copy'].round().astype(int)
    df['building_floor_num'] = df['building_floor_num'].round().astype(int)

rent_map = train.groupby('flat_rooms')['flat_rent'].median()
deposit_map = train.groupby('flat_rooms')['flat_deposit'].median()

rent_map_all = train['flat_rent'].median()
deposit_map_all = train['flat_deposit'].median()

train['flat_rent'] = train['flat_rent'].fillna(train['flat_rooms'].map(rent_map))
train['flat_deposit'] = train['flat_deposit'].fillna(train['flat_rooms'].map(deposit_map))

test['flat_rent'] = test['flat_rent'].fillna(test['flat_rooms'].map(rent_map))
test['flat_deposit'] = test['flat_deposit'].fillna(test['flat_rooms'].map(deposit_map))

train['flat_rent'] = train['flat_rent'].fillna(rent_map_all)
train['flat_deposit'] = train['flat_deposit'].fillna(deposit_map_all)
test['flat_rent'] = test['flat_rent'].fillna(rent_map_all)
test['flat_deposit'] = test['flat_deposit'].fillna(deposit_map_all)

train['quarter'] = le.inverse_transform(train['quarter_copy'].astype(int))
test['quarter'] = le.inverse_transform(test['quarter_copy'].astype(int))

prestige_names = (
    r'city park|wojskow[ay]|towarow[ay]|maraton|soln[ay]|za bramką|wejchert|atal|garbar|'
    r'plac wolności|chwaliszewo|bałtyk|wilsona|zajezdnia|bookowska|stary browar|'
    r'ostrów tumski|nowa wilda|perfumiarnia|manufaktura'
)

premium_words = (
        'apartament|premium|luksus|wysoki standard|modern|design|loft|penthouse|smart|'
        'klimatyzac|taras|widok|panorama|saun|siłowni|fitness|ochron|monitor|'
        'concierge|granit|marmur|podłogow|antresola'
    )

budget_words = r'tani|okazj|student|skromn|\bpokój\b|\bpokoj\b|odstepne|odstępne|prl'

for df in [train, test]:
    title_low = df['ad_title'].str.lower()

    df['is_prestige'] = title_low.str.contains(prestige_names, na=False).astype(int)
    df['is_premium']  = title_low.str.contains(premium_words, na=False).astype(int)
    df['is_budget']   = title_low.str.contains(budget_words, na=False).astype(int)

