rf_model = RandomForestRegressor(n_estimators=100, random_state=42)
rf_model.fit(X_train_v, y_train_v)
rf_val_pred = rf_model.predict(X_val)

# XGBoost
y_train_v_log = np.log1p(y_train_v)
xgb_model = xgb.XGBRegressor(**xgb_params)
