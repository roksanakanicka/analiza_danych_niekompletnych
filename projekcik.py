xgb_val_pred_log = xgb_model.predict(X_val)
xgb_val_pred = np.expm1(xgb_val_pred_log)


final_val_pred = (rf_val_pred + xgb_val_pred) / 2

mae = mean_absolute_error(y_val, final_val_pred)
