"""## **Modele RandomForest i XGBoost**"""

to_drop = ['id', 'ad_title', 'date_activ', 'date_modif', 'date_expire', 'price', 'quarter']
features = [col for col in train.columns if col.lower() not in to_drop]

X_train = train[features]
y_train = train['price']
X_test = test[features]

model = RandomForestRegressor(n_estimators=100, random_state=42)
model.fit(X_train, y_train)
rf_pred = model.predict(X_test)

xgb_params = {
    'n_estimators': 1500,
    'learning_rate': 0.015,
    'max_depth': 7,
    'subsample': 0.8,
    'random_state': 42,
    'n_jobs': -1
}

y_train_log = np.log1p(train['price'])

model_xgb = xgb.XGBRegressor(**xgb_params)

model_xgb.fit(X_train, y_train_log)

predictions_log = model_xgb.predict(X_test)
xgb_pred = np.expm1(predictions_log)

final_pred = (rf_pred + xgb_pred) / 2
submission = pd.DataFrame({
    'ID': range(1, len(test) + 1),
    'TARGET': final_pred
})

